package com.parkhon.dabyss.game.system.collision.grid;

import java.util.ArrayList;

public class TileCollection
{
    //Variables----------------------------------------------------------
    //-------------------------------------------------------------------
    //-------------------------------------------------------------------

    //Grid Data
    private int x;
    private int y;
    private ArrayList<GridObject> gridObjects;

    //Constructor--------------------------------------------------------
    //-------------------------------------------------------------------
    //-------------------------------------------------------------------
    public TileCollection(int x, int y)
    {
        this.x = x;
        this.y = y;
        gridObjects = new ArrayList<>();
    }

    //Overrides----------------------------------------------------------
    //-------------------------------------------------------------------
    //-------------------------------------------------------------------

    //Methods------------------------------------------------------------
    //-------------------------------------------------------------------
    //-------------------------------------------------------------------
    public ArrayList<GridObject> getGridObjects()
    {
        return  gridObjects;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    //Technical Methods--------------------------------------------------
    //-------------------------------------------------------------------
    //-------------------------------------------------------------------

}
